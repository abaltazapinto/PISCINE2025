int	ft_str_is_printable(char *str)
{
	int	n;

	n = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[n] != '\0')
	{
		if ((str[n] >= 32 && str[n] <= 127))
			n++;
		else
			return (0);
	}
	return (1);
}
