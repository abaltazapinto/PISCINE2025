int	ft_str_is_numeric(char *str)
{
	int	n;

	n = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[n] != '\0')
	{
		if ((str[n] >= '0' && str[n] <= '9'))
			n++;
		else
			return (0);
	}
	return (1);
}
